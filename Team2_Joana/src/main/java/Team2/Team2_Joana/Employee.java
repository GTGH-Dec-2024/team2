package Team2.Team2_Joana;

public class Employee {
	private String name;
	private String surname;
	private String email;
	/**
	 * @param name
	 * @param surname
	 * @param email
	 */
	public Employee(String name, String surname, String email) {
		super();
		this.name = name;
		this.surname = surname;
		this.email = email;
	}
	
}
